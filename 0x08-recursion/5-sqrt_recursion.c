/**
 * _sqrt_recursion - returns the natural square root of a number
 *
* @a: The number
 * @b: The number to test for the square root of @a
 * 
 * @n: the number to return the square root of
 *
 * Return: the square root of @n
 */

int _sqrt_recursion(int n)
{

    int a, b;
    if (b * b > a)
    {
        return (-1);

    }
	else if (b * b == a)
    {
            return (b);
    }
	else
    {
        	return (a, b + 1);

	return (1);
    }
	
	if (n == 0){
        return (0);
    }
		
	return (n, 1);
}
